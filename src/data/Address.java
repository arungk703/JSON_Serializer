package data;

public class Address {

	private String houseNumber;
	private String area;
	private String city;
	private String state;
	private String country;

	public Address(String houseNumber, String area, String city, String state, String country) {
		this.houseNumber = houseNumber;
		this.area = area;
		this.city = city;
		this.state = state;
		this.country = country;
	}

}
